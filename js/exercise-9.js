// ЗАДАЧА: Реєстрація користувачів та підрахунок їхнього віку
//  Використовуємо цикл do...while
//  Перевіряємо введені дані
//  Працюємо з об’єктами та масивами
// Що має відбуватися?
// Користувач вводить ім'я та вік.
// Якщо ім'я порожнє або вік не є числом, запит повторюється.
// Якщо натиснути "Скасувати", введення завершується.
// Дані зберігаються у вигляді об'єкта { name, age } і додаються до масиву users.
// Після завершення введення в консоль виводиться:
// Повний список користувачів у форматі:
// Ім'я: Іван, Вік: 25
// Ім'я: Марія, Вік: 30
// Наймолодший користувач
// Найстарший користувач
//  Приклад виводу в консоль
// Введіть ім'я: Іван
// Введіть вік: 25
// Введіть ім'я: Марія
// Введіть вік: 30
// Введіть ім'я: (натиснуто "Скасувати")

// Список користувачів:
// Ім'я: Іван, Вік: 25
// Ім'я: Марія, Вік: 30

// Наймолодший: Іван (25 років)
// Найстарший: Марія (30 років)

const users = [];

do {
  const name = prompt("Введіть ім'я");
  if (name === null || name.trim() === '') {
    break;
  }

  const age = prompt('Введіть вік');
  if (isNaN(Number(age)) || age === null || age.trim() === '') {
    alert('Введіть коректно вік');
    continue;
  }

  users.push({ name, age: Number(age) });

} while (true);

if (users.length === 0) {
  console.log("Не введено жодного ім'я.");
  
}

for (const user of users) {
  console.log(`Ім'я: ${user.name}, Вік: ${user.age}`);
  }

let youngest = users[0];
let oldest = users[0];

for (let i = 1; i < users.length; i++) {
  if (users[i].age < youngest.age) {
     youngest = users[i];
  }
  if (users[i].age > oldest.age) {
    oldest = users[i];
  } 

console.log(`Наймолодший: ${youngest.name} - ${youngest.age} років`);
console.log(`Найстарший: ${oldest.name} - ${oldest.age} років`);

}